#!/usr/bin/env node

/**
 * Generates PRINCIPLES/halos-principles-v1.0.md from spec/core.json.
 *
 * core.json is the single source of truth for HALOS principles (D6).
 * This script renders the human-readable markdown version.
 *
 * Usage: node scripts/generate-principles.js
 */

import { readFileSync, writeFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..');

const core = JSON.parse(readFileSync(resolve(root, 'spec/core.json'), 'utf-8'));
const outputPath = resolve(root, 'PRINCIPLES/halos-principles-v1.0.md');

// Map requirement IDs to section numbers
const sectionNumber = (id) => parseInt(id.replace('HALOS-CORE-', ''), 10);

// Expand terse descriptions into principle prose.
// The descriptions in core.json are normative one-liners. The generated
// markdown preserves them as-is — editorial expansion belongs in the
// halos website, not here.

const lines = [
  '# HALOS Principles v1.0',
  '',
  '> **Generated from `spec/core.json` — do not edit this file directly.**',
  '> To update principles, edit `spec/core.json` and run `node scripts/generate-principles.js`.',
  '',
  'These principles anchor the HALOS framework. They are intended to change rarely. Implementations, specifications, and proposals should be evaluated against them.',
  '',
  '---',
  '',
];

for (const req of core.requirements) {
  const num = sectionNumber(req.id);
  lines.push(`## ${num}. ${req.title}`);
  lines.push('');
  lines.push(req.description);
  lines.push('');
  lines.push(`*Requirement ID: ${req.id}*`);
  lines.push('');
}

lines.push('---');
lines.push('');
lines.push(`*These principles are stable. Proposals that would materially alter them require exceptional justification and broad consensus.*`);
lines.push('');
lines.push(`*Generated from spec/core.json v${core.version}*`);
lines.push('');

writeFileSync(outputPath, lines.join('\n'));
console.log(`Generated ${outputPath}`);
